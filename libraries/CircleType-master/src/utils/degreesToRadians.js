const radiansPerDegree = Math.PI / 180;

/**
 * Converts an angle in degrees to its equivalent in radians.
 *
 * @param {number} angleInDegrees The angle in degrees to convert.
 *
 * @return {number} The equivalent angle in radians.
 */
export default angleInDegrees => angleInDegrees * radiansPerDegree;
